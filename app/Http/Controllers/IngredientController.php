    {
        $query = Ingredient::query()->orderFromRequest($request);
        $search = $request->get('q');

        if ($search) {
            $query->where('name', 'like', '%' . $search . '%');
        }

        return Inertia::render('ingredients/index', [
            'q' => $search,
            'collection' => IngredientResource::collection(
